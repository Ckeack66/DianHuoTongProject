package com.mhky.dianhuotong.shop.precenter;

import com.lzy.okgo.OkGo;
import com.lzy.okgo.callback.Callback;
import com.lzy.okgo.model.Progress;
import com.lzy.okgo.model.Response;
import com.lzy.okgo.request.base.Request;
import com.mhky.dianhuotong.base.BaseApplication;
import com.mhky.dianhuotong.base.BaseTool;
import com.mhky.dianhuotong.base.BaseUrlTool;
import com.mhky.dianhuotong.shop.shopif.ShopCredentialIF;

/**
 * Created by Administrator on 2018/5/2.
 */

public class ShopCredentialPresenter {
    private ShopCredentialIF shopCredentialIF;

    public ShopCredentialPresenter(ShopCredentialIF shopCredentialIF) {
        this.shopCredentialIF = shopCredentialIF;
    }

    /**
     * 获取当前店铺已经上传的资质
     * 需传入店铺id
     */
    public void getCredential() {
        OkGo.<String>get(BaseUrlTool.getShopCredentialUrl(BaseApplication.getInstansApp().getLoginRequestInfo().getShopId() + "")).execute(new Callback<String>() {
            @Override
            public void onStart(Request<String, ? extends Request> request) {

            }

            @Override
            public void onSuccess(Response<String> response) {
                shopCredentialIF.getShopCredentialSucess(response.code(), BaseTool.getResponsBody(response));
            }

            @Override
            public void onCacheSuccess(Response<String> response) {

            }

            @Override
            public void onError(Response<String> response) {
                shopCredentialIF.getShopCredentialFaild(response.code(), BaseTool.getResponsBody(response));
            }

            @Override
            public void onFinish() {

            }

            @Override
            public void uploadProgress(Progress progress) {

            }

            @Override
            public void downloadProgress(Progress progress) {

            }

            @Override
            public String convertResponse(okhttp3.Response response) throws Throwable {
                return null;
            }
        });
    }

    /**
     * 上传新资质
     */

    public void uploadNewCredential(String json) {
        OkGo.<String>post(BaseUrlTool.UPLOAD_CREDENTIAL).upJson(json).execute(new Callback<String>() {
            @Override
            public void onStart(Request<String, ? extends Request> request) {

            }

            @Override
            public void onSuccess(Response<String> response) {
                shopCredentialIF.uploadShopCredentialSucess(response.code(), BaseTool.getResponsBody(response));
            }

            @Override
            public void onCacheSuccess(Response<String> response) {

            }

            @Override
            public void onError(Response<String> response) {
                shopCredentialIF.uploadShopCredentialFaild(response.code(), BaseTool.getResponsBody(response));
            }

            @Override
            public void onFinish() {

            }

            @Override
            public void uploadProgress(Progress progress) {

            }

            @Override
            public void downloadProgress(Progress progress) {

            }

            @Override
            public String convertResponse(okhttp3.Response response) throws Throwable {
                return null;
            }
        });
    }

    /**
     * 修改已有资质
     */
    public void updateCredential(String ID, String json) {
        OkGo.<String>put(BaseUrlTool.getUpdateCredentialUrl(ID)).upJson(json).execute(new Callback<String>() {
            @Override
            public void onStart(Request<String, ? extends Request> request) {

            }

            @Override
            public void onSuccess(Response<String> response) {
                shopCredentialIF.updateShopCredentialSucess(response.code(), BaseTool.getResponsBody(response));
            }

            @Override
            public void onCacheSuccess(Response<String> response) {

            }

            @Override
            public void onError(Response<String> response) {
                shopCredentialIF.updateShopCredentialFaild(response.code(), BaseTool.getResponsBody(response));
            }

            @Override
            public void onFinish() {

            }

            @Override
            public void uploadProgress(Progress progress) {

            }

            @Override
            public void downloadProgress(Progress progress) {

            }

            @Override
            public String convertResponse(okhttp3.Response response) throws Throwable {
                return null;
            }
        });
    }




}
