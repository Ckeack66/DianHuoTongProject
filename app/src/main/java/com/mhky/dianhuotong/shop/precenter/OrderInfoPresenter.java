package com.mhky.dianhuotong.shop.precenter;

import com.lzy.okgo.OkGo;
import com.lzy.okgo.callback.Callback;
import com.lzy.okgo.model.Progress;
import com.lzy.okgo.model.Response;
import com.lzy.okgo.request.base.Request;
import com.mhky.dianhuotong.base.BaseTool;
import com.mhky.dianhuotong.base.BaseUrlTool;
import com.mhky.dianhuotong.shop.shopif.OrderInfoIF;

import java.util.HashMap;

public class OrderInfoPresenter {

    public OrderInfoPresenter setOrderInfoIF(OrderInfoIF orderInfoIF) {
        this.orderInfoIF = orderInfoIF;
        return this;
    }

    private OrderInfoIF orderInfoIF;

    public void getOrderInfoByNumber(String orderNumber) {
        HashMap hashMap = new HashMap<>();
        hashMap.put("orderNo", orderNumber);
        OkGo.<String>get(BaseUrlTool.GET_ORDER_INFO + BaseTool.getUrlParamsByMap(hashMap, false)).execute(new Callback<String>() {
            @Override
            public void onStart(Request<String, ? extends Request> request) {

            }

            @Override
            public void onSuccess(Response<String> response) {
                orderInfoIF.getOrderInfoSuccess(response.code(), BaseTool.getResponsBody(response));
            }

            @Override
            public void onCacheSuccess(Response<String> response) {

            }

            @Override
            public void onError(Response<String> response) {
                orderInfoIF.getOrderInfoFailed(response.code(), BaseTool.getResponsBody(response));
            }

            @Override
            public void onFinish() {

            }

            @Override
            public void uploadProgress(Progress progress) {

            }

            @Override
            public void downloadProgress(Progress progress) {

            }

            @Override
            public String convertResponse(okhttp3.Response response) throws Throwable {
                return null;
            }
        });
    }
}
